const mergeTranslations = (base, overrides) => {
    return Object.entries(overrides).reduce((merged, [key, value]) => {
        if (
            value
            && typeof value === 'object'
            && !Array.isArray(value)
            && base[key]
            && typeof base[key] === 'object'
            && !Array.isArray(base[key])
        ) {
            return {
                ...merged,
                [key]: mergeTranslations(base[key], value),
            };
        }

        return {
            ...merged,
            [key]: value,
        };
    }, { ...base });
};

