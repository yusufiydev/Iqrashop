const getValue = (obj, path) => {
    return path.split('.').reduce((acc, key) => (acc && key in acc ? acc[key] : undefined), obj);
};

const withParams = (text, params = {}) => {
    if (typeof text !== 'string') return text;
    return text.replace(/\{\{(\w+)\}\}/g, (_, key) => String(params[key] ?? ''));
};

export const normalizeLanguage = (lang) => {
    if (SUPPORTED_LANGUAGES.includes(lang)) return lang;
    return DEFAULT_LANGUAGE;
};

export const getStoredLanguage = () => {
    if (typeof window === 'undefined') return DEFAULT_LANGUAGE;
    return normalizeLanguage(localStorage.getItem('book_market_language'));
};

export const getStoredTheme = () => {
    if (typeof window === 'undefined') return DEFAULT_THEME;
    const stored = localStorage.getItem('book_market_theme');
    return stored === 'dark' ? 'dark' : DEFAULT_THEME;
};

export const translate = (key, language = DEFAULT_LANGUAGE, params = {}) => {
    const lang = normalizeLanguage(language);
    const direct = getValue(translations[lang], key);
    if (direct !== undefined) return withParams(direct, params);

    const fallback = getValue(translations[DEFAULT_LANGUAGE], key);
    if (fallback !== undefined) return withParams(fallback, params);

    return key;
};
